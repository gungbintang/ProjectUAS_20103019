    private void tlabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tlabelMouseClicked
        String urlGambarNull = "src/img/gambar.jpg";
        this.txNIDN.setText(model.getValueAt(tlabel.getSelectedRow(),0).toString());
        this.txNama.setText(model.getValueAt(tlabel.getSelectedRow(),1).toString());
        this.txProdi.setText(model.getValueAt(tlabel.getSelectedRow(),2).toString());
        String txJK = model.getValueAt(tlabel.getSelectedRow(),3).toString();
        if(txJK.equals("Perempuan")){
            this.txJKP.setSelected(true);
        }else{
            this.txJKP.setSelected(true);
        }
        this.txTGL.setText(model.getValueAt(tlabel.getSelectedRow(),4).toString());
        String urlGambar = "src/img/"+this.txNIDN.getText()+".jpg";
        BufferedImage loadImg = img.loadImage(urlGambar);
        if(loadImg == null){
            loadImg = img.loadImage(urlGambarNull);
        }
        ImageIcon imageIcon = new ImageIcon (loadImg);
        this.imgphoto.setIcon(imageIcon);
        
        this.lbCRUDds.setText("Edit Data"+this.txNIDN.getText());
        this.cmdTambah.setEnabled(false);
        this.cmdUpdate.setEnabled(true);
        this.cmdDelete.setEnabled(true);
    }//GEN-LAST:event_tlabelMouseClicked

