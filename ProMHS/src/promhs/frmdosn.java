    private void initTable() {
        model = new DefaultTableModel();
        tlabel.setModel(model);
        model.addColumn("NIDN");
        model.addColumn("NAMA DOSEN");
        model.addColumn("PRODI");
        model.addColumn("JENIS KELAMIN");
        model.addColumn("TANGGAL LAHIR");
    }
    

    private void getdata() {
        String sql = "Select NIDN, NAMA_DOSEN, PRODI, JENIS_KELAMIN, TGL_LAHIR FROM datadosen;";
    model.getDataVector().removeAllElements();
    model.fireTableDataChanged();
    try{
        this.tampildata(sql);
        while(this.rs.next()){
            Object[] obj = new Object[5];
            obj[0]= this.rs.getString("NIDN");
            obj[1]= this.rs.getString("NAMA_DOSEN");
            obj[2]= this.rs.getString("PRODI");
            String txJK = this.rs.getString("JENIS_KELAMIN");
            if(txJK.equals("L")){
                obj[3]="Laki-Laki";
            }else{
                obj[3]="Perempuan";
            }
            obj[4]= this.rs.getString("TGL_LAHIR");
        model.addRow(obj);
        }
    }catch(Exception e){
    }
    } 
