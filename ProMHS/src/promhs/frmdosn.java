    private void cmdUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdUpdateActionPerformed
        String NIDN = this.txNIDN.getText();
        String NAMA = this.txNama.getText();
        String PRODI = this.txProdi.getText();
        String JNS_KELAMIN = "L";
        if(this.txJKP.isSelected()){
            JNS_KELAMIN="P";
        }
        String TGL = this.txTGL.getText();
        String SQLUpdate = "UPDATE datadosen SET NAMA_DOSEN='"+NAMA+"', PRODI='"+PRODI+"',JENIS_KELAMIN='"+JNS_KELAMIN+"',TGL_LAHIR='"+TGL+"' WHERE NIDN='"+NIDN+"';";
        if(this.UpdateData(SQLUpdate)){
            this.getdata();
            JOptionPane.showMessageDialog(null, "Data Terupdate");
        }
                                             
    }//GEN-LAST:event_cmdUpdateActionPerformed

    private void cmdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDeleteActionPerformed
        
        String NIDN=this.txNIDN.getText();
        String NAMA = this.txNama.getText();
        int opsi = JOptionPane.showConfirmDialog(null,"yakin Akan Menghapus "+NAMA+"("+NIDN+")?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        if(opsi == JOptionPane.YES_OPTION){
            String SQLDelete="DELETE FROM datadosen WHERE NIDN='"+NIDN+"';";
            if(this.UpdateData(SQLDelete)){
                this.getdata();
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            }
        }
    }//GEN-LAST:event_cmdDeleteActionPerformed

